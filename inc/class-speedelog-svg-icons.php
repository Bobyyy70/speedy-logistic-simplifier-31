        /**
         * Gets the SVG code for a given icon.
         *
         * @param string $icon  The icon name.
         * @param array  $args {
         *     Optional. Arguments to configure the SVG.
         *
         *     @type string $size   The size of the SVG icon. Default: '24'.
         *     @type string $class  Additional CSS class. Default: ''.
         * }
         * @return string The SVG code.
         */
        public static function get_svg($icon, $args = array()) {
            $defaults = array(
                'size'  => '24',
                'class' => '',
            );

            $args = wp_parse_args($args, $defaults);

            $svg_icons = self::get_icons();

            if (array_key_exists($icon, $svg_icons)) {
                $svg = $svg_icons[$icon];

                $size_class = $args['size'] ? ' speedelog-icon-' . $args['size'] : '';

                $class = 'speedelog-icon' . $size_class . ' ' . $args['class'];
                $class = trim($class);

                // Add extra attributes
                $extra_attributes = '';
                if (!empty($args['size'])) {
                    $extra_attributes .= ' width="' . esc_attr($args['size']) . '" height="' . esc_attr($args['size']) . '"';
                }

                // Replace the class and attributes
                $svg = str_replace('class="', 'class="' . esc_attr($class) . ' ', $svg);
                $svg = str_replace('<svg', '<svg' . $extra_attributes, $svg);

                return $svg;
            }

            return '';
        }

        /**
         * Display the SVG.
         *
         * @param string $icon The icon name.
         * @param array  $args Optional arguments.
         */
        public static function display($icon, $args = array()) {
            echo self::get_svg($icon, $args); // phpcs:ignore WordPress.Security.EscapeOutput.OutputNotEscaped
        }

